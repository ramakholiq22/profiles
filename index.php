<!-- JS untuk Animasi scroll ketika user klik tombol dengan tag <a> -->
<script>
    $(document).ready(function(){
        $("a").on('click', function(event){
            if (this.hash !== ""){
                event.preventDefault();
                var hash = this.hash;

                $('html, body').animate({
                    scrollTop: $(hash).offset().top
                }, 800, function(){
                    window.location.hash = hash;
                });
            }
        });
    });
</script>
